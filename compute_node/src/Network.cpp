{
}

//--------------------------------------------------------------------------------------------------
void Network::createStimulus ()
{
    for (int i = 0; i < stimulusArray.size (); ++i)
        for (int j = 0; j < stimulusArray[i].size (); ++j)
            if (stimulusArray[i][j])
                addNode (stimulus_active, stimulusXstart + i * stimulusXstep, stimulusY, stimulusZstart + j * stimulusZstep);
            else
                addNode (stimulus_rest,   stimulusXstart + i * stimulusXstep, stimulusY, stimulusZstart + j * stimulusZstep);
}

//--------------------------------------------------------------------------------------------------
void Network::createNetwork ()
{
    createNeuron ();
}
