//--------------------------------------------------------------------------------------------------
void Network::addNode (NodeType const type, float const x, float const y, float const z)
{
    nodeTypes.push_back (type);
    nodes.push_back (x);
    nodes.push_back (y);
    nodes.push_back (z);
}

//--------------------------------------------------------------------------------------------------
void Network::createNeuron ()
{
    normal_distribution<> normal {neuronXmean, neuronXstd};
    lognormal_distribution<> logNormal {0, 0.5};

    for (int i = 0; i < nodesPerNeuron; ++i)
    {
        addNode (node, normal (rnd), stimulusY + 100 * logNormal (rnd), normal (rnd));
    }
}

